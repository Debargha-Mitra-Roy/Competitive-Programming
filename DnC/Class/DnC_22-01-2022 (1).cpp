#include <bits/stdc++.h>

using namespace std;

int main()
{

    // Declare variable a
    int a;

    // Declare Variable b
    int b;

    // Take input a and b
    cin >> a >> b;

    // Store the addition of a and b in c
    int c = a + b;

    // Print the value of a and b
    cout << " a = " << a << "\n";
    cout << " b = " << b << "\n";

    // Output the value in c
    cout << "Output is : " << c << "\n";
}